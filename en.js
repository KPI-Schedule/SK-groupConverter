module.exports = function en(letter) {
  switch(letter) {
    case 'а': return 'a';
    case 'б': return 'b';
    case 'в': return 'v';
    case 'г': return 'g';
    case 'д': return 'd';
    case 'е': return 'e';
    case 'з': return 'z';
    case 'і': return 'i';
    case 'к': return 'k';
    case 'л': return 'l';
    case 'м': return 'm';
    case 'н': return 'n';
    case 'о': return 'o';
    case 'п': return 'p';
    case 'р': return 'r';
    case 'с': return 's';
    case 'т': return 't';
    case 'у': return 'u';
    case 'ф': return 'f';
    case 'х': return 'h';
    case 'ц': return 'c';
    case 'я': return 'y';
  }
}