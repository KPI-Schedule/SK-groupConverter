module.exports = function ru(letter) {
  switch(letter) {
    case 'а': return 'а';
    case 'б': return 'б';
    case 'в': return 'в';
    case 'г': return 'г';
    case 'д': return 'd';
    case 'е': return 'e';
    case 'з': return 'з';
    case 'і': return 'и';
    case 'к': return 'к';
    case 'л': return 'л';
    case 'м': return 'м';
    case 'н': return 'н';
    case 'о': return 'о';
    case 'п': return 'п';
    case 'р': return 'р';
    case 'с': return 'с';
    case 'т': return 'т';
    case 'у': return 'у';
    case 'ф': return 'ф';
    case 'х': return 'х';
    case 'ц': return 'ц';
    case 'я': return 'я';
  }
}